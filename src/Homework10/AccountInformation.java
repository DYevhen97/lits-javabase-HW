package Homework10;

public class AccountInformation {

    public String name;
    public String surname;
    public int age;
    public String position;
    public String phoneNumber;
    public String email;
    public String address;
    public String role;

    public AccountInformation (String name, String surname, int age, String position, String phoneNumber, String email, String address, String role) {
        this.name = name;
        this.surname = surname;
        this.age = age;
        this.position = position;
        this.phoneNumber = phoneNumber;
        this.email = email;
        this.address = address;
        this.role = role;
    }

}
