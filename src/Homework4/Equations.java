package Homework4;

public class Equations {

    public void adding(Calculator calculator) {
        calculator.a = 20;
        calculator.b = 10;
        calculator.c = calculator.a + calculator.b;
    }
    public void subtracting(Calculator calculator) {
        calculator.a = 20;
        calculator.b = 10;
        calculator.c = calculator.a - calculator.b;
    }
    public void multiplication(Calculator calculator) {
        calculator.a = 20;
        calculator.b = 10;
        calculator.c = calculator.a * calculator.b;
    }
    public void division(Calculator calculator) {
        calculator.a = 20;
        calculator.b = 10;
        calculator.c = calculator.a / calculator.b;
    }
}
